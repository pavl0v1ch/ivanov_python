text = input("Введите строку: ")

# Инициализируется переменная compressed — она будет содержать результат сжатия.
# Переменная count отслеживает количество повторяющихся символов подряд.
compressed = ""
count = 1

# Проход по строке начинается со второго символа (индекс 1).
# Сравниваются текущий и предыдущий символы:
# Если они совпадают — счётчик увеличивается.
# Если различаются — в compressed добавляется предыдущий символ и его количество.
# Затем счётчик сбрасывается на 1.
for i in range(1, len(text)):
    if text[i] == text[i - 1]:
        count += 1  # Увеличивается счётчик повторов
    else:
        compressed += text[i - 1] + str(count)  # Добавляется символ и количество повторов
        count = 1  # Сброс счётчика

# После завершения цикла добавляется последний символ и его количество.
# Это необходимо, так как последний блок не попадает в основной цикл.
if text:
    compressed += text[-1] + str(count)

print("Сжатая строка:", compressed)