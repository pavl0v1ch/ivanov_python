import numpy as np

# Матрица коэффициентов A
A = np.array([
    [-2.0, -8.5, -3.2,  3.5],
    [ 0.0,  2.4,  2.4,  1.0],
    [ 2.5,  0.6, -2.4,  2.8],
    [ 3.5,  1.2,  2.5, -2.6]
])

# Вектор правых частей B
B = np.array([-1.88, -3.28, 0.63, -2.01])

# Решение системы: X = A⁻¹ · B
X = np.linalg.solve(A, B)

# Округление до одного знака после запятой
X_rounded = np.round(X, 1)

# Вывод
print("Решение системы:")
for i, x in enumerate(X_rounded, start=1):
    print(f"x{i} = {x}")
